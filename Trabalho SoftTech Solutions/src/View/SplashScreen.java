    public SplashScreen() {
        initComponents();
        Menu login = new Menu();
        new Thread() {
            public void run() {
                try {
                    for (int i = 0; i <= 100; i++) {
                        Thread.sleep(100);
                        telaCarregamento.setValue(i);
                        if (telaCarregamento.getValue() <= 25) {
                            lblStatus.setText("Bem vindo(a) a SoftTech Solutions");
                        } else if (telaCarregamento.getValue() <= 50) {
                            lblStatus.setText("Acessando banco de dados...");
                        } else if (telaCarregamento.getValue() <= 75) {
                            lblStatus.setText("Conectando alunos e professores...");
                        } else if (telaCarregamento.getValue() <= 99) {
                            lblStatus.setText("Tudo pronto.");
                        } else {
                            dispose();
                            login.setVisible(true);
                        }
                    }
                } catch (InterruptedException ex) {
                }
            }
        }.start();
    }

